type EOFVersion = u8;

#[derive(Eq, PartialEq, Debug, Clone, Serialize, Deserialize)]
pub struct EOFContainer {
    version: EOFVersion,
    sections: Vec<EOFSection>,
}

#[derive(Eq, PartialEq, Debug, Clone, Serialize, Deserialize)]
pub enum EOFSection {
    Code(Vec<u8>),
    Data(Vec<u8>),
    Type(Vec<EOFTypeSectionEntry>),
}

#[derive(Eq, PartialEq, Debug, Clone, Serialize, Deserialize)]
pub struct EOFTypeSectionEntry {
    inputs: u8,
    outputs: u8,
}

impl EOFSection {
    fn kind(&self) -> u8 {
        match self {
            EOFSection::Code(_) => 1,
            EOFSection::Data(_) => 2,
            EOFSection::Type(_) => 3,
        }
    }
}

